package org.example

fun main() {
    var vocal = 'A'

    println("Vocal " + vocal++)
    println("Vocal " + vocal++)
    println("Vocal " + vocal++)
    println("Vocal " + vocal--)
    println("Vocal " + vocal--)
    println("Vocal " + vocal--)
    println("Vocal " + vocal--)

}
// Yang menarik, kita bisa melakukan operasi
// increment (++) dan decrement (--) pada
//sebuah variabel dengan tipe data Char